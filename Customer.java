public class Customer {
    private String name;
    private int pin;

    public Customer() {
        this.name = name;
        this.pin = pin;
    }

    public int getPin() {
        return pin;
    }

    public String getName() {
        return name;
    }

    public void setPin(int newPin) {
        pin = newPin;
    }

    public void setName(String newName) {
        name = newName;
    }
}

